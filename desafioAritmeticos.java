package oii;

public class desafioAritmeticos {
    public static void main(String[] args) {
        int a = (3+2);
        int b = 6;
        int c = (b * a /2);
        int d = (int)Math.pow (c, 2);

        int e = d / 3*2;

        int f = (1-5) * (2-7)/2;
        int g = (int)Math.pow (f, 2);

        int h = e - g;
        int i = (int)Math.pow(h, 3);

        int j = 10;
        int k = (int)Math.pow(j, 3);

        int l = i / k;
        System.out.println(l);
    }
}
